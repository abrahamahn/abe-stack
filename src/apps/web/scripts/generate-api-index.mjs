// src/apps/web/scripts/generate-api-index.mjs
import { readdirSync, writeFileSync } from 'node:fs';
import { dirname, extname, join, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const apiDir = join(__dirname, '..', 'src', 'api');
const indexPath = join(apiDir, 'index.ts');

const toCamelCase = (value) =>
  value
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .trim()
    .split(/\s+/)
    .map((part, index) => {
      if (index === 0) return part.toLowerCase();
      return part.charAt(0).toUpperCase() + part.slice(1).toLowerCase();
    })
    .join('');

const findRouteFiles = (dir, acc = []) => {
  const entries = readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      findRouteFiles(fullPath, acc);
      continue;
    }
    if (!entry.isFile()) continue;
    if (extname(entry.name) !== '.ts') continue;
    if (entry.name !== 'route.ts') continue;
    acc.push(fullPath);
  }
  return acc;
};

const routeFiles = findRouteFiles(apiDir).sort();
const routeExports = routeFiles.map((fullPath) => {
  const routeDir = dirname(fullPath);
  const relDir = relative(apiDir, routeDir).split(sep).join('/');
  const exportName = `${toCamelCase(relDir)}Route`;
  return `export * as ${exportName} from './${relDir}/route';`;
});

const output = [
  '// src/apps/web/src/api/index.ts',
  '// AUTO-GENERATED by scripts/generate-api-index.mjs',
  '// Run: pnpm -C src/apps/web run api:index',
  "export { ApiProvider, useApi } from './ApiProvider';",
  ...routeExports,
  '',
].join('\n');

writeFileSync(indexPath, output, 'utf8');
